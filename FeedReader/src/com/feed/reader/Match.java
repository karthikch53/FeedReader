package com.feed.reader;

public class Match 
{
	private String matchName;
	private String matchLoc;
	private String score;
	private String plScore;
	private String matchStatus;
	public String getMatchName() {
		return matchName;
	}
	public void setMatchName(String matchName) {
		this.matchName = matchName;
	}
	public String getMatchLoc() {
		return matchLoc;
	}
	public void setMatchLoc(String matchLoc) {
		this.matchLoc = matchLoc;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getPlScore() {
		return plScore;
	}
	public void setPlScore(String plScore) {
		this.plScore = plScore;
	}
	public String getMatchStatus() {
		return matchStatus;
	}
	public void setMatchStatus(String matchStatus) {
		this.matchStatus = matchStatus;
	}
}
